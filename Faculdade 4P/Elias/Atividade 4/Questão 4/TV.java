public class TV {
    private int canal;
    private int volume;

    public TV() {
        this.canal = 0;
        this.volume = 0;
    }

    public int getCanal() {
        return canal;
    }

    public void setCanal(int canal) {
        this.canal = canal;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
}
