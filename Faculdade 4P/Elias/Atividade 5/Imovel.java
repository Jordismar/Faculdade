public class Imovel {
    private String endereco;
    private float valor;

    public Imovel() {
        this.endereco = "";
        this.valor = 0;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

}
